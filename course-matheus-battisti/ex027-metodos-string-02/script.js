let frase = "Hello, World!";

// lowerCase
let fraseLower = frase.toLowerCase();
console.log(fraseLower);

// upperCase
let fraseUpper = frase.toUpperCase();
console.log(fraseUpper);

// Trim
let nome = "   Mat ";
console.log(nome);
let nomeTrim = nome.trim();
console.log(nomeTrim);

// Split
console.log(frase.split(""));
console.log(frase.split(", "));

// lastIndexOf
let frase2 = "Oi Mat, Oi";
console.log(frase2.indexOf("Oi"));
console.log(frase2.lastIndexOf("Oi"));
